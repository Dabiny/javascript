/*
    그래프 (Graph)
    정점과 간선으로 구성되어 네트워크 구조를 추상화한 비선형 자료구조

    - 그래프 특징
    * 정점(vertex == node)과 간선(edge)의 집합
    * 다양한 그래프 종류를 혼합하여 표현가능
    
    - 그래프 종류
    * 방향 그래프(Directed Graph): 간선에 특정 방향이 존재하는 그래프 (A -> B로 표현, A에서 B로만 이동가능)
    * 무방향 그래프(Undirected Graph): 간선에 특정 방향이 존재하지 않는 그래프 ( A - B로 표현, 양방향 이동가능)
    * 가중치 그래프(Weighted Grapth): 간선에 비용이나 가중치가 할당된 그래프

    * 연결 그래프(Connected Graph): 무방향 그래프에 있는 모든 정점쌍에 대해 항상 경로가 존재하는 그래프
    * 비연결 그래프(DisConnected Graph): 무방향 그래프에서 특정 정점쌍 사이에 경로가 존재하지 않는 그래프
    * 순환 그래프(Cycle): 단순경로의 시작정점과 종료 지점이 동일하여 순환지점이 존재하는 그래프
    * 비순환 그래프(Acyclic Graph): 순환 지점이 존재하지 않는 그래프
    * 완전 그래프(Complete Graph): 그래프에 속해 있는 모든 정점이 연결되어 있는 그래프

    - 그래프 표현방법
    * 인접리스트(Adjacent List): 정점에 연결된 다른 정점을 리스트로 표현 (연결배열, Dictionary, hash...)
    * 인접행렬(Adjacency Matrix): 정점에 연결된 다른 정점을 정점*정점 크기의 매트릭스로 표현한다. (2차원배열..)
    (필기그림 참고)
*/